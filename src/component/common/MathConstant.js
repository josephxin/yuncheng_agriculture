/**
 * π, 180 degrees
 * @type {number}
 */
const PI = Math.PI;
/**
 * double π, 360 degrees
 * @type {number}
 */
const DP = PI * 2;
/**
 * half shadow-pig π, 90 degrees
 * @type {number}
 */
const HP = PI / 2;
/**
 * shadow-pig quarter of π, 45 degrees
 * @type {number}
 */
const QP = PI / 4;

export {
  PI,
  DP,
  HP,
  QP
};

/**
 * degree to radian
 * @type {number}
 */
const UA = PI / 180;
/**
 * radian to degree
 * @type {number}
 */
const AU = 180 / PI;

export {
  UA,
  AU
};
